<h1 class="page-header">About eAuction</h1>
eAuction is a privately owned, for-profit company set-up by a group of students from The University of Manchester.<br><br>
eAuction is a marketplace that allows users to offer, sell and buy just about anything.<br><br>
eAuction does not have possession of anything listed or sold through eAuction, and is not involved in the actual transaction between buyers and sellers.<br><br>
The contract for the sale is directly between buyer and seller.
